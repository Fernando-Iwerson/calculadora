function calculadora(n1,n2,operador){
    

    if(operador == '+'){
        console.log(n1+n2);
    }
    else if(operador == '-'){
        console.log(n1-n2);
    }
    else if(operador == '*'){
        console.log(n1*n2);
    }
    else if(operador == '/'){
        console.log(n1/n2);
    }
    else {
        console.log(0)
    }
}
calculadora(7,3,'*');





